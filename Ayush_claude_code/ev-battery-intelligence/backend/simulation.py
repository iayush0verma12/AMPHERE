"""
In-memory "live" fleet state: loads the synthetic seed data generated by
train.py, then advances each vehicle by one telemetry tick on a timer so the
dashboard has a real streaming feel. A real deployment replaces this module
with a CAN-bus/BMS ingestion consumer -- the row schema and fault-evaluation
call are the integration point to keep.
"""

from __future__ import annotations

import json
import os
import threading
import time

import numpy as np
import pandas as pd

from ml.data_generator import N_CELLS, NOMINAL_CELL_V
from ml.fault_detection import _with_derived_columns, evaluate_row

ARTIFACT_DIR = os.path.join(os.path.dirname(__file__), "artifacts")
SEED_PATH = os.path.join(ARTIFACT_DIR, "fleet_seed.json")
MAX_HISTORY_ROWS = 400
TICK_SECONDS = 4


class VehicleState:
    def __init__(self, vehicle_id: str, profile: dict, history: pd.DataFrame):
        self.vehicle_id = vehicle_id
        self.profile = profile
        self.history = history
        self.active_fault_type = profile.get("fault_injection")
        self.ticks_since_fault = 999 if self.active_fault_type else 0
        self.rng = np.random.default_rng(abs(hash(vehicle_id)) % (2**32))
        self.tick_index = len(history)

    def inject_fault(self, fault_type: str | None):
        self.active_fault_type = fault_type
        self.ticks_since_fault = 0

    def tick(self, anomaly_model):
        last = self.history.iloc[-1]
        rng = self.rng

        next_ts = pd.Timestamp(last["timestamp"]) + pd.Timedelta(minutes=30)
        self.tick_index += 1

        soc = 50 + 45 * np.sin(self.tick_index / 10.0)
        soc = float(np.clip(soc + rng.normal(0, 2), 5, 100))
        current = rng.uniform(20, 80) if soc >= last["soc"] else -rng.uniform(20, 120)

        cycle_count = float(last["cycle_count"]) + 0.05
        self.profile["cycle_count"] = cycle_count
        self.profile["calendar_age_days"] = float(self.profile.get("calendar_age_days", 0)) + 0.02

        avg_temp = self.profile["avg_temp"]
        pack_temp = avg_temp + rng.normal(0, 1.5) + abs(current) * 0.03

        pack_voltage_target = N_CELLS * NOMINAL_CELL_V * (0.85 + 0.15 * soc / 100)
        cell_voltages = np.full(N_CELLS, pack_voltage_target / N_CELLS) + rng.normal(0, 0.01, N_CELLS)

        if self.active_fault_type:
            self.ticks_since_fault += 1
            t = min(self.ticks_since_fault, 30)
            if self.active_fault_type == "THERMAL_EVENT":
                pack_temp += np.interp(t, [0, 30], [2, 32])
            elif self.active_fault_type == "CELL_IMBALANCE":
                weak_cell = self.tick_index % N_CELLS
                cell_voltages[weak_cell] -= np.interp(t, [0, 30], [0.02, 0.18])
            elif self.active_fault_type == "SENSOR_DROPOUT":
                dropout_cell = (self.tick_index // 3) % N_CELLS
                cell_voltages[dropout_cell] = 0.0
            elif self.active_fault_type == "OVERVOLTAGE":
                cell_voltages += np.interp(t, [0, 30], [0.05, 0.5])
            elif self.active_fault_type == "UNDERVOLTAGE":
                cell_voltages -= np.interp(t, [0, 30], [0.05, 1.0])

        row = {
            "timestamp": next_ts,
            "cycle_count": cycle_count,
            "soc": soc,
            "soh": float(last["soh"]) - 0.001,
            "pack_voltage": float(cell_voltages.sum()),
            "pack_current": float(current),
            "pack_temp": float(pack_temp),
            "fault_label": self.active_fault_type or "",
        }
        for i in range(N_CELLS):
            row[f"cell_{i+1}_v"] = float(cell_voltages[i])

        new_row_df = pd.DataFrame([row])
        self.history = pd.concat([self.history, new_row_df], ignore_index=True).tail(MAX_HISTORY_ROWS)

        derived = _with_derived_columns(self.history.tail(1)).iloc[0]
        faults = evaluate_row(derived, anomaly_model)
        return faults


class FleetSimulator:
    def __init__(self):
        with open(SEED_PATH) as f:
            seed = json.load(f)

        self.vehicles: dict[str, VehicleState] = {}
        for vid, payload in seed.items():
            df = pd.DataFrame(payload["history"])
            df["timestamp"] = pd.to_datetime(df["timestamp"])
            self.vehicles[vid] = VehicleState(vid, payload["profile"], df)

        self._lock = threading.Lock()
        self._latest_faults: dict[str, list[dict]] = {}
        self._anomaly_model = None
        self._stop = False

    def set_anomaly_model(self, model):
        self._anomaly_model = model

    def latest_faults(self, vehicle_id: str) -> list[dict]:
        return self._latest_faults.get(vehicle_id, [])

    def inject_fault(self, vehicle_id: str, fault_type: str | None):
        with self._lock:
            self.vehicles[vehicle_id].inject_fault(fault_type)

    def _tick_all(self):
        with self._lock:
            for vid, vstate in self.vehicles.items():
                faults = vstate.tick(self._anomaly_model)
                self._latest_faults[vid] = faults

    def run_forever(self):
        while not self._stop:
            time.sleep(TICK_SECONDS)
            try:
                self._tick_all()
            except Exception as exc:  # keep the sim loop alive across bad ticks
                print(f"[simulation] tick error: {exc}")

    def start_background_thread(self):
        thread = threading.Thread(target=self.run_forever, daemon=True)
        thread.start()
        return thread
